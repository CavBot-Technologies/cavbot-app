function computeSeedIndex(seed: string, length: number) {
  let hash = 5381;
  for (let i = 0; i < seed.length; i += 1) {
    hash = (hash * 33) ^ seed.charCodeAt(i);
  }
  return (hash >>> 0) % length;
}

export function getRandomStatusMessage(status: IncidentStatus, seed?: string): string {
  const pool = STATUS_MESSAGE_POOLS[status];
  if (!pool || pool.length === 0) {
    return STATUS_RESPONSE_MESSAGES[status] ?? "";
  }
  const index = seed ? computeSeedIndex(seed, pool.length) : Math.floor(Math.random() * pool.length);
  return pool[index];
}

