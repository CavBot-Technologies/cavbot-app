type MessageParts = {
  leadIns: string[];
  verbs: string[];
  focuses: string[];
  intents: string[];
  qualifiers?: string[];
  limit: number;
};

function buildMessagePool(parts: MessageParts) {
  const { leadIns, verbs, focuses, intents, qualifiers = [""] } = parts;
  const pool: string[] = [];

  outer: for (const lead of leadIns) {
    for (const verb of verbs) {
      for (const focus of focuses) {
        for (const intent of intents) {
          for (const qualifier of qualifiers) {
            const base = [lead, verb, focus, intent].filter(Boolean).join(" ");
            const message = qualifier ? `${base} ${qualifier}.` : `${base}.`;
            pool.push(message);
            if (pool.length >= parts.limit) {
              break outer;
            }
          }
        }
      }
    }
  }

  return pool;
}

